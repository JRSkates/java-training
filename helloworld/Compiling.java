public class Compiling {
  public static void main(String[] args) {
    
    System.out.println("Java is a class-based language.");
    System.out.println("Java classes have a 'main' method.");
    System.out.println("Java statements end with a semicolon.");

    System.out.println("Programming is... fun!");
    
  }
}

// java - The Java compiler command used to compile Java source code into bytecode.
// Compiling - The process of converting Java source code (written in .java files) into
// bytecode (stored in .class files) that can be executed by the Java Virtual Machine (JVM).
// javac - The Java compiler executable that is used to compile Java source files.
// java - The command used to run Java applications. It executes the bytecode produced by the compiler.

// javac Compiling.java - This command compiles the Compiling.java source file and produces a Compiling.class file containing the bytecode.
// java Compiling - This command runs the Compiling class by executing the bytecode